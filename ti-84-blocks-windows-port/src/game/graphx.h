void gfx_SetDrawBuffer() {
    draw_ui = false;
}

void gfx_SetDrawScreen() {
    draw_ui = true;
}

static inline void gfx_SetColor(uint8_t color) {
    gfx_color = color;
}

static inline void gfx_SetTextFGColor(uint8_t color) {
    gfx_text_fgcolor = color;
}

static inline void gfx_FillScreen(uint8_t color) {
    memset(ui_buffer, color, LCD_WIDTH * LCD_HEIGHT);
}

static inline void gfx_DrawPixel(int x, int y) {
    if (x >= 0 && x < LCD_WIDTH && y >= 0 && y < LCD_HEIGHT) {
        ui_buffer[y * LCD_WIDTH + x] = gfx_color;
    }
}

static inline void gfx_DrawPixelText(int x, int y) {
    if (x >= 0 && x < LCD_WIDTH && y >= 0 && y < LCD_HEIGHT) {
        ui_buffer[y * LCD_WIDTH + x] = gfx_text_fgcolor;
    }
}

static inline void gfx_Rectangle(int x1, int y1, int w, int h) {
    int x2 = x1 + w;
    int y2 = y1 + h;

    for (int x = x1; x <= x2; x++) {
        gfx_DrawPixel(x, y1);
        gfx_DrawPixel(x, y2);
    }
    for (int y = y1; y <= y2; y++) {
        gfx_DrawPixel(x1, y);
        gfx_DrawPixel(x2, y);
    }
}

static inline void gfx_FillRectangle(int x1, int y1, int w, int h) {
    int x2 = x1 + w;
    int y2 = y1 + h;

    for (int y = y1; y <= y2; y++) {
        for (int x = x1; x <= x2; x++) {
            gfx_DrawPixel(x, y);
        }
    }
}

static inline void gfx_DrawChar(char c, int x, int y) {
    const uint8_t* glyph = font8x8[c];
    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            if (glyph[row] & (1 << col)) {  // Fix: Remove (7 - col) mirroring
                gfx_DrawPixelText(x + col, y + row);
            }
        }
    }
}

static inline void gfx_PrintStringXY(const char* text, int x, int y) {
    int i = 0;
    while (text[i] != '\0') {
        gfx_DrawChar(text[i], x + (i * 8), y);
        i++;
    }
}

static inline uint16_t gfx_RGBTo1555(uint8_t r, uint8_t g, uint8_t b, uint8_t alpha = 0)
{
    uint16_t a = (alpha > 0) ? 0x8000 : 0x0000;  // Set alpha bit if alpha > 0
    uint16_t r5 = (r >> 3) & 0x1F;  // Convert 8-bit to 5-bit
    uint16_t g5 = (g >> 3) & 0x1F;
    uint16_t b5 = (b >> 3) & 0x1F;

    return a | (r5 << 10) | (g5 << 5) | b5;
}

static inline ImU32 gfx_1555ToImGuiColor(uint16_t color1555)
{
    uint8_t alpha = (color1555 & 0x8000) ? 0 : 255;  // Extract alpha bit
    uint8_t r = ((color1555 >> 10) & 0x1F) * 255 / 31; // Convert 5-bit to 8-bit
    uint8_t g = ((color1555 >> 5) & 0x1F) * 255 / 31;
    uint8_t b = (color1555 & 0x1F) * 255 / 31;

    return IM_COL32(r, g, b, alpha);  // ImGui's RGBA format
}
